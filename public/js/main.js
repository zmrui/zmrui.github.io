(() => {
  // <stdin>
  console.log("This site was generated by Hugo.");
  document.addEventListener("DOMContentLoaded", function() {
    const anchorLinks = document.querySelectorAll('a[href^="#"]');
    anchorLinks.forEach((link) => {
      link.addEventListener("click", function(e) {
        e.preventDefault();
        const targetId = this.getAttribute("href").substring(1);
        const targetElement = document.getElementById(targetId);
        if (targetElement) {
          const mobileOutline2 = document.getElementById("mobile-outline");
          if (mobileOutline2) {
            mobileOutline2.classList.add("hidden");
          }
          const offsetTop = targetElement.offsetTop - 20;
          window.scrollTo({
            top: offsetTop,
            behavior: "smooth"
          });
          targetElement.style.transition = "background-color 0.3s ease";
          targetElement.style.backgroundColor = "#f0f9ff";
          setTimeout(() => {
            targetElement.style.backgroundColor = "";
          }, 2e3);
        }
      });
    });
    const outlineToggle = document.getElementById("outline-toggle");
    const outlineClose = document.getElementById("outline-close");
    const mobileOutline = document.getElementById("mobile-outline");
    if (outlineToggle && mobileOutline) {
      outlineToggle.addEventListener("click", function() {
        mobileOutline.classList.remove("hidden");
      });
    }
    if (outlineClose && mobileOutline) {
      outlineClose.addEventListener("click", function() {
        mobileOutline.classList.add("hidden");
      });
    }
    if (mobileOutline) {
      mobileOutline.addEventListener("click", function(e) {
        if (e.target === mobileOutline) {
          mobileOutline.classList.add("hidden");
        }
      });
    }
    function updateActiveSection() {
      const sections = document.querySelectorAll("section[id]");
      const navLinks = document.querySelectorAll('nav a[href^="#"], #mobile-outline a[href^="#"]');
      let currentSection = "";
      const scrollPosition = window.scrollY + 100;
      sections.forEach((section) => {
        const top = section.offsetTop;
        const height = section.offsetHeight;
        if (scrollPosition >= top && scrollPosition < top + height) {
          currentSection = section.getAttribute("id");
        }
      });
      navLinks.forEach((link) => {
        link.classList.remove("bg-blue-100", "text-blue-800", "font-semibold");
        link.classList.add("text-blue-600");
        if (link.getAttribute("href") === "#" + currentSection) {
          link.classList.remove("text-blue-600");
          link.classList.add("bg-blue-100", "text-blue-800", "font-semibold");
        }
      });
    }
    window.addEventListener("scroll", updateActiveSection);
    updateActiveSection();
  });
})();
